from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True)
class AccidentTravelInput:
    full_name: str
    contact: str
    days: int
    age: int
    variant_a: bool
    variant_b: bool
    sum_a: int | None  # 30000/50000/70000/100000
    sum_b: int | None  # 5000/10000/20000
    territory_option: int  # AB5: 1..5
    sport_training: bool  # A2
    insured_count: int  # AB2
    repeat_contract: bool  # AB3.1
    e_policy: bool  # AB3.2


@dataclass(frozen=True)
class AccidentTravelQuote:
    premium: float
    currency: str
    breakdown: list[tuple[str, float]]


def _pick_rate(days: int, rows: list[tuple[int, int, tuple[float, ...]]], index: int) -> float:
    for d_from, d_to, vals in rows:
        if d_from <= days <= d_to:
            return vals[index]
    raise ValueError("Нет тарифа для указанной продолжительности")


def _ab1_age_coef(age: int) -> float:
    if age <= 2:
        return 0.90
    if 3 <= age <= 16:
        return 0.50
    if 17 <= age <= 59:
        return 1.00
    if 60 <= age <= 65:
        return 1.20
    if 66 <= age <= 70:
        return 2.50
    if 71 <= age <= 75:
        return 3.50
    return 5.00


def _ab2_count_coef(cnt: int) -> float:
    if cnt >= 20:
        return 0.80
    if cnt >= 10:
        return 0.90
    return 1.00


def _ab3_coef(repeat_contract: bool, e_policy: bool) -> float:
    # AB3.1 и AB3.2 альтернативные; берём более "льготный" (минимальный)
    vals = [1.00]
    if repeat_contract:
        vals.append(0.85)
    if e_policy:
        vals.append(0.90)
    return min(vals)


def _ab5_coef(option: int) -> float:
    return {
        1: 1.00,
        2: 1.10,
        3: 2.50,
        4: 4.50,
        5: 4.60,
    }.get(option, 1.00)


