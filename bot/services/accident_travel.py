def calculate_accident_travel(inp: AccidentTravelInput) -> AccidentTravelQuote:
    if not inp.variant_a and not inp.variant_b:
        raise ValueError("Нужно выбрать хотя бы один вариант страхования: A и/или B")
    if inp.days < 1 or inp.days > 366:
        raise ValueError("Срок поездки должен быть от 1 до 366 дней")
    if inp.age < 0 or inp.age > 120:
        raise ValueError("Возраст должен быть в диапазоне 0-120")

    ab1 = _ab1_age_coef(inp.age)
    ab2 = _ab2_count_coef(inp.insured_count)
    ab3 = _ab3_coef(inp.repeat_contract, inp.e_policy)
    ab5 = _ab5_coef(inp.territory_option)
    a2 = 1.50 if inp.sport_training else 1.00

    breakdown: list[tuple[str, float]] = [
        ("AB1 Возраст", ab1),
        ("AB2 Количество застрахованных", ab2),
        ("AB3 История/e-полис", ab3),
        ("AB5 Территория поездки", ab5),
    ]

    total = 0.0

    if inp.variant_a:
        if inp.sum_a not in {30000, 50000, 70000, 100000}:
            raise ValueError("Для варианта A доступны суммы: 30000/50000/70000/100000")
        idx = {30000: 0, 50000: 1, 70000: 2, 100000: 3}[inp.sum_a]
        base_a = _pick_rate(inp.days, _A_ROWS, idx)
        total_a = base_a * ab1 * ab2 * ab3 * ab5 * a2
        # A1 для 71-90 дней и суммы 30k-100k, но не при AB5 >= 2.00
        a1 = 1.00
        if 71 <= inp.days <= 90 and ab5 < 2.00:
            a1 = 0.85
            total_a *= a1
        total += total_a
        breakdown.append((f"A Базовый тариф (сумма {inp.sum_a})", base_a))
        if inp.sport_training:
            breakdown.append(("A2 Спорт/тренировки", a2))
        if a1 != 1.00:
            breakdown.append(("A1 71-90 дней", a1))

    if inp.variant_b:
        if inp.sum_b not in {5000, 10000, 20000}:
            raise ValueError("Для варианта B доступны суммы: 5000/10000/20000")
        idx = {5000: 0, 10000: 1, 20000: 2}[inp.sum_b]
        base_b = _pick_rate(inp.days, _B_ROWS, idx)
        total_b = base_b * ab1 * ab2 * ab3 * ab5
        total += total_b
        breakdown.append((f"B Базовый тариф (сумма {inp.sum_b})", base_b))

    return AccidentTravelQuote(premium=round(total, 2), currency="USD/EUR", breakdown=breakdown)
